#include <iostream>
using namespace std;

int main()
{
    int Ghifar;
    cout << "Menampilkan Angka 20 - 1 (Descending)" << endl;
    cout << "~~~~~~~~~~~~~~~~~~~~~~~~~~~" << endl;
    cout << endl;
	
    for (Ghifar = 20; Ghifar >= 1; Ghifar--)
        cout << Ghifar << endl;

    cin.get();
}
